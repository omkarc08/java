package org.conditions;

public class If_else_ex1 {

	public static void main(String[] args) {
		
		double math = 75;
		double science = 80;
		
		if (math>=75) {
		System.out.println("you are passed in math");
		}
		if(science>=75) {
			System.out.println("you are passed in science");
		}
		else {
			System.out.println("you are failed in math and science");
		}
		

	}

}
